let dynamicCurriculumData: any = null;

export const setDynamicCurriculum = (data: any) => {
    dynamicCurriculumData = data;
};

export const getCurriculumData = () => {
    return dynamicCurriculumData || CURRICULUM_DATA;
};

export const getSubjectsForGradeAndStream = (grade: string, stream: string) => {
    const data = getCurriculumData();
    const allSubjects = Object.keys(data[grade] || {});
    if (!stream) return allSubjects;

    const allowedSubjects = STREAM_SUBJECT_MAPPING[stream] || [];
    return allSubjects.filter(s => allowedSubjects.includes(s));
};

export const getSubjectsForGrade = (grade: string) => {
    return getSubjectsForGradeAndStream(grade, "");
};

export const getChaptersForSubject = (grade: string, subject: string) => {
    const data = getCurriculumData();
    return data[grade]?.[subject] || [];
};
